package de.hpi.oryxengine.activity;

/**
 * This enum represents the execution states an activities may be in.
 */
public enum ActivityState {
    
    /** The INIT. */
    INIT,
    
    /** The READY. */
    READY,
    
    /** The ACTIVE. */
    ACTIVE,
    
    /** The COMPLETED. */
    COMPLETED,
    
    /** The SKIPPED. */
    SKIPPED,
    
    /** The SUSPENDED. */
    SUSPENDED
}
